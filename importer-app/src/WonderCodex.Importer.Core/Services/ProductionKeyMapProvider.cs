            _ => default
        };

        return schema.SaveVersion != 0;
    }

    public bool Supports(JsonElement root) => TryGetSchema(root, out _);

    public bool LooksCompact(JsonElement root)
    {
        if (root.ValueKind != JsonValueKind.Object) return false;

        var total = 0;
        var shortNames = 0;
        foreach (var property in root.EnumerateObject())
        {
            total++;
            if (property.Name.Length <= 4) shortNames++;
        }

        return total > 0 && shortNames == total;
    }
